#!/usr/bin/env python3
"""
Verification script for zeta-zero amplitudes and Möbius cancellation sums.

Notes:
- The prompt's "zeta(rho_k)" constants are the derivative values zeta'(rho_k),
  which is exactly what mpmath.zeta(s, derivative=1) returns.
- Möbius values are generated by a pure-Python sieve; no file I/O, no extra deps.
"""

from mpmath import mp
import sys

# ---------------------------------------------------------------------
# Precision and constants
# ---------------------------------------------------------------------

mp.dps = 50

ZERO_COUNT = 20
K_VALUES = [10, 20, 50, 100, 200, 500]
K_SET = set(K_VALUES)
K_MAX = max(K_VALUES)

EMPIRICAL_R2 = mp.mpf("0.944")
SANITY_TOL = mp.mpf("1e-12")


# ---------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------

def mobius_sieve(n: int):
    """
    Linear sieve for Möbius mu(n) on 1..n.
    mu(1) = 1
    """
    mu = [0] * (n + 1)
    mu[1] = 1
    primes = []
    is_composite = [False] * (n + 1)

    for i in range(2, n + 1):
        if not is_composite[i]:
            primes.append(i)
            mu[i] = -1
        for p in primes:
            j = i * p
            if j > n:
                break
            is_composite[j] = True
            if i % p == 0:
                mu[j] = 0
                break
            mu[j] = -mu[i]

    return mu


def fmt(x, digits=18):
    """Compact mpmath-aware formatter for real or complex values."""
    return mp.nstr(x, n=digits)


def print_table(title, headers, rows):
    """Simple aligned table printer."""
    string_rows = [[str(cell) for cell in row] for row in rows]
    widths = [len(h) for h in headers]
    for row in string_rows:
        for i, cell in enumerate(row):
            widths[i] = max(widths[i], len(cell))

    header_fmt = " | ".join("{:>%d}" % w for w in widths)
    row_fmt = " | ".join("{:>%d}" % w for w in widths)
    rule = "-+-".join("-" * w for w in widths)

    print(title)
    print(header_fmt.format(*headers))
    print(rule)
    for row in string_rows:
        print(row_fmt.format(*row))
    print()


# ---------------------------------------------------------------------
# Sanity check
# ---------------------------------------------------------------------

def verify_startup_constants():
    rho1 = mp.zetazero(1)
    zeta_prime_rho1 = mp.zeta(rho1, derivative=1)

    rho1_expected = mp.mpc(mp.mpf("0.5"), mp.mpf("14.134725141734693"))
    zeta_prime_rho1_expected = mp.mpc(
        mp.mpf("0.783296511867031"),
        mp.mpf("0.124699829748171"),
    )
    abs_zeta_prime_rho1_expected = mp.mpf("0.793160433356506")

    if abs(rho1 - rho1_expected) > SANITY_TOL:
        raise SystemExit(
            "Startup sanity check failed for rho_1:\n"
            f"  computed = {fmt(rho1, 30)}\n"
            f"  expected = {fmt(rho1_expected, 30)}\n"
            f"  abs diff = {fmt(abs(rho1 - rho1_expected), 20)}"
        )

    if abs(zeta_prime_rho1 - zeta_prime_rho1_expected) > SANITY_TOL:
        raise SystemExit(
            "Startup sanity check failed for zeta'(rho_1):\n"
            f"  computed = {fmt(zeta_prime_rho1, 30)}\n"
            f"  expected = {fmt(zeta_prime_rho1_expected, 30)}\n"
            f"  abs diff = {fmt(abs(zeta_prime_rho1 - zeta_prime_rho1_expected), 20)}"
        )

    if abs(abs(zeta_prime_rho1) - abs_zeta_prime_rho1_expected) > SANITY_TOL:
        raise SystemExit(
            "Startup sanity check failed for |zeta'(rho_1)|:\n"
            f"  computed = {fmt(abs(zeta_prime_rho1), 30)}\n"
            f"  expected = {fmt(abs_zeta_prime_rho1_expected, 30)}\n"
            f"  abs diff = {fmt(abs(abs(zeta_prime_rho1) - abs_zeta_prime_rho1_expected), 20)}"
        )

    print("Sanity check passed at 50-digit precision.")
    print(f"  rho_1                 = {fmt(rho1, 30)}")
    print(f"  zeta'(rho_1)          = {fmt(zeta_prime_rho1, 30)}")
    print(f"  |zeta'(rho_1)|        = {fmt(abs(zeta_prime_rho1), 30)}")
    print()


# ---------------------------------------------------------------------
# Main computation
# ---------------------------------------------------------------------

def main():
    verify_startup_constants()

    mu = mobius_sieve(K_MAX)

    print("Using zeta'(rho_k) from mpmath.zeta(rho_k, derivative=1).")
    print("The partial sums c_K(rho_k) are compared to 1 / zeta'(rho_k).")
    print()

    zeros = []
    zeta_prime_vals = []
    targets = []
    amplitudes = []
    denominators = []

    convergence_rows = []

    for k in range(1, ZERO_COUNT + 1):
        rho = mp.zetazero(k)
        zeta_prime_rho = mp.zeta(rho, derivative=1)
        target = mp.mpf(1) / zeta_prime_rho

        denom = abs(rho * zeta_prime_rho)
        amp = mp.mpf(2) / denom

        zeros.append(rho)
        zeta_prime_vals.append(zeta_prime_rho)
        targets.append(target)
        amplitudes.append(amp)
        denominators.append(denom)

        s = mp.mpc(0)
        checkpoint_idx = 0
        for j in range(2, K_MAX + 1):
            mj = mu[j]
            if mj:
                s += mj * mp.power(j, -rho)

            if checkpoint_idx < len(K_VALUES) and j == K_VALUES[checkpoint_idx]:
                ratio = s / target
                abs_err = abs(s - target)
                convergence_rows.append([
                    k,
                    j,
                    fmt(ratio, 18),
                    fmt(abs_err, 12),
                ])
                checkpoint_idx += 1

    # -----------------------------------------------------------------
    # Convergence table
    # -----------------------------------------------------------------

    print_table(
        "Convergence table: c_K(rho_k) / (1 / zeta'(rho_k)) and absolute error",
        ["k", "K", "ratio", "|c_K - 1/zeta'(rho_k)|"],
        convergence_rows,
    )

    # -----------------------------------------------------------------
    # Amplitude table
    # -----------------------------------------------------------------

    amplitude_rows = []
    for k in range(1, ZERO_COUNT + 1):
        amplitude_rows.append([
            k,
            fmt(zeros[k - 1], 20),
            fmt(denominators[k - 1], 20),
            fmt(amplitudes[k - 1], 20),
        ])

    print_table(
        "Amplitude table: a_k = 2 / |rho_k * zeta'(rho_k)|",
        ["k", "rho_k", "|rho_k * zeta'(rho_k)|", "a_k"],
        amplitude_rows,
    )

    # -----------------------------------------------------------------
    # Cumulative R^2 table
    # -----------------------------------------------------------------

    total_a2 = mp.fsum([a * a for a in amplitudes])
    cum_a2 = mp.mpf(0)
    r2_values = []
    r2_rows = []

    for m in range(1, ZERO_COUNT + 1):
        cum_a2 += amplitudes[m - 1] * amplitudes[m - 1]
        r2 = cum_a2 / total_a2
        r2_values.append(r2)
        r2_rows.append([
            m,
            fmt(cum_a2, 20),
            fmt(r2, 20),
            fmt(r2 - EMPIRICAL_R2, 20),
        ])

    print_table(
        "Cumulative R^2 table (empirical reference R^2 = 0.944)",
        ["m", "sum_{k=1}^m a_k^2", "theoretical R^2", "theoretical - 0.944"],
        r2_rows,
    )

    closest_m = min(range(1, ZERO_COUNT + 1), key=lambda m: abs(r2_values[m - 1] - EMPIRICAL_R2))
    closest_r2 = r2_values[closest_m - 1]
    print(f"Total sum of a_k^2 over k=1..{ZERO_COUNT}: {fmt(total_a2, 20)}")
    print(
        "Closest theoretical cumulative R^2 to 0.944 occurs at "
        f"m={closest_m}: {fmt(closest_r2, 20)} "
        f"(delta = {fmt(closest_r2 - EMPIRICAL_R2, 20)})"
    )


if __name__ == "__main__":
    main()
