# ── Prime sieve ────────────────────────────────────────────────────────
def sieve_primes(n):
    """Return list of primes up to n using Sieve of Eratosthenes."""
    is_prime = np.ones(n + 1, dtype=bool)
    is_prime[:2] = False
    for i in range(2, int(n**0.5) + 1):
        if is_prime[i]:
            is_prime[i*i::i] = False
    return np.where(is_prime)[0]

def first_n_primes(n):
    """Return the first n primes."""
    # Upper bound for the n-th prime: p_n < n*(ln(n) + ln(ln(n))) for n >= 6
    if n < 6:
        upper = 15
    else:
        ln_n = np.log(n)
        upper = int(n * (ln_n + np.log(ln_n)) * 1.2) + 100
    primes = sieve_primes(upper)
    while len(primes) < n:
        upper = int(upper * 1.5)
        primes = sieve_primes(upper)
    return primes[:n]

# ── Mertens spectroscope ──────────────────────────────────────────────
def mertens_spectroscope(values, gammas):
    """
    Compute the Mertens spectroscope: for each gamma,
    S(gamma) = -sum_{p in values} cos(gamma * log(p))
    (For primes, Mobius mu(p) = -1.)
    """
    log_vals = np.log(values.astype(float))
    spectrum = np.zeros(len(gammas))
    for i, g in enumerate(gammas):
        spectrum[i] = -np.sum(np.cos(g * log_vals))
    return spectrum

