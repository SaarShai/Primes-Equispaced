# APPLICATION 1: PRNG AUDITING
# ══════════════════════════════════════════════════════════════════════
report("# Practical Applications of the Mertens Spectroscope")
report()
report("*Generated by practical_applications_test.py*")
report()
report("---")
report()
report("## Application 1: Pseudo-Random Number Generator Auditing")
report()
report("### Method")
report("- Generate the first 10,000 real primes and 10,000 fake 'primes' (random odd numbers in the same range).")
report("- Compute the Mertens spectroscope S(γ) = −Σ cos(γ·log(p)) for γ ∈ [0.1, 50] with step 0.01.")
report("- Compare: do real primes show peaks at zeta-zero heights while fake primes do not?")
report()

N = 10000
primes = first_n_primes(N)
p_max = primes[-1]
p_min = primes[0]

# Fake primes: random odd numbers in [p_min, p_max]
random.seed(42)
fake_primes = set()
while len(fake_primes) < N:
    r = random.randint(p_min, p_max)
    if r % 2 == 0:
        r += 1
    fake_primes.add(r)
fake_primes = np.sort(np.array(list(fake_primes)))[:N]

gammas = np.arange(0.1, 50.01, 0.01)

print("Computing Application 1: PRNG auditing...")
t0 = time.time()
spec_real = mertens_spectroscope(primes, gammas)
spec_fake = mertens_spectroscope(fake_primes, gammas)
t1 = time.time()
print(f"  Done in {t1-t0:.1f}s")

# Correlation and L2
corr = np.corrcoef(spec_real, spec_fake)[0, 1]
l2_diff = np.sqrt(np.sum((spec_real - spec_fake)**2))

# Peak analysis near zeta zeros
report("### Results")
report()
report(f"- Number of real primes: {len(primes)}, range [{primes[0]}, {primes[-1]}]")
report(f"- Number of fake primes: {len(fake_primes)}, range [{fake_primes[0]}, {fake_primes[-1]}]")
report(f"- Spectral correlation (real vs fake): **{corr:.4f}**")
report(f"- L2 spectral difference: **{l2_diff:.2f}**")
report()
report("| Zeta Zero γ | Real Peak γ | Real Peak Val | Fake Peak γ | Fake Peak Val | Distinguishable? |")
report("|-------------|-------------|---------------|-------------|---------------|------------------|")

distinguishable_count = 0
for z in ZETA_ZEROS[:5]:
    # Search in window [z-1, z+1]
    mask = (gammas >= z - 1.0) & (gammas <= z + 1.0)
    idx_r = np.argmax(np.abs(spec_real[mask]))
    idx_f = np.argmax(np.abs(spec_fake[mask]))
    g_r = gammas[mask][idx_r]
    g_f = gammas[mask][idx_f]
    v_r = np.abs(spec_real[mask][idx_r])
    v_f = np.abs(spec_fake[mask][idx_f])
    dist = "Yes" if v_r > 1.5 * v_f else "No"
    if dist == "Yes":
        distinguishable_count += 1
    report(f"| {z:.4f} | {g_r:.2f} | {v_r:.2f} | {g_f:.2f} | {v_f:.2f} | {dist} |")

report()

# Subtract smooth baseline and re-compare
from scipy.ndimage import uniform_filter1d
baseline_real = uniform_filter1d(spec_real, size=200)
baseline_fake = uniform_filter1d(spec_fake, size=200)
residual_real = spec_real - baseline_real
residual_fake = spec_fake - baseline_fake

# Residual peak analysis
report("#### Baseline-subtracted residual analysis")
report()
report("| Zeta Zero γ | Resid Real Peak | Resid Fake Peak | Ratio (R/F) |")
report("|-------------|-----------------|-----------------|-------------|")

resid_ratios = []
for z in ZETA_ZEROS[:5]:
    mask = (gammas >= z - 0.5) & (gammas <= z + 0.5)
    v_r = np.max(np.abs(residual_real[mask]))
    v_f = np.max(np.abs(residual_fake[mask]))
    ratio = v_r / max(v_f, 1e-10)
    resid_ratios.append(ratio)
    report(f"| {z:.4f} | {v_r:.2f} | {v_f:.2f} | {ratio:.2f} |")

avg_ratio = np.mean(resid_ratios)
report()
report(f"Average residual ratio at zeta zeros: **{avg_ratio:.2f}**")
report()

# Plot
fig, axes = plt.subplots(2, 1, figsize=(14, 10))

ax = axes[0]
ax.plot(gammas, spec_real, 'b-', alpha=0.7, linewidth=0.5, label='Real primes')
ax.plot(gammas, spec_fake, 'r-', alpha=0.7, linewidth=0.5, label='Fake primes')
for z in ZETA_ZEROS[:5]:
    ax.axvline(z, color='green', alpha=0.4, linestyle='--', linewidth=1)
ax.set_xlabel('γ')
ax.set_ylabel('S(γ)')
ax.set_title('Application 1: Mertens Spectroscope — Real vs Fake Primes (Raw)')
ax.legend()

ax = axes[1]
ax.plot(gammas, residual_real, 'b-', alpha=0.7, linewidth=0.5, label='Real (residual)')
ax.plot(gammas, residual_fake, 'r-', alpha=0.7, linewidth=0.5, label='Fake (residual)')
for z in ZETA_ZEROS[:5]:
    ax.axvline(z, color='green', alpha=0.4, linestyle='--', linewidth=1)
ax.set_xlabel('γ')
ax.set_ylabel('Residual S(γ)')
ax.set_title('Application 1: Baseline-subtracted Residuals')
ax.legend()

plt.tight_layout()
fig.savefig(FIG_DIR / "app1_prng_auditing.png", dpi=150)
plt.close(fig)
report("![Application 1](figures/app1_prng_auditing.png)")
report()

if avg_ratio > 1.5:
    feasibility = "MEDIUM"
    report("### Feasibility: MEDIUM")
    report()
    report("The baseline-subtracted residuals show measurable differences at zeta-zero heights,")
    report(f"with real primes averaging {avg_ratio:.2f}x the fake prime residuals.")
elif distinguishable_count >= 3:
    feasibility = "MEDIUM"
    report("### Feasibility: MEDIUM")
