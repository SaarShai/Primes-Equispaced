# APPLICATION 3: ANOMALY DETECTION IN PRIME-LIKE SEQUENCES
# ══════════════════════════════════════════════════════════════════════
report("## Application 3: Anomaly Detection in Prime-Like Sequences")
report()
report("### Method")
report("- Take first 10,000 primes. Remove k random primes for k ∈ {10, 50, 100, 200, 500, 1000}.")
report("- Compute spectroscope on gapped sequence vs full sequence.")
report("- Measure relative L2 difference: ||S_gap - S_full|| / ||S_full||.")
report("- Average over 20 random trials per k value.")
report()

primes_3 = first_n_primes(10000)
gammas_3 = np.arange(0.1, 50.01, 0.05)

print("Computing Application 3: Anomaly detection...")
t0 = time.time()

spec_full = mertens_spectroscope(primes_3, gammas_3)
norm_full = np.sqrt(np.sum(spec_full**2))

removal_counts = [10, 50, 100, 200, 500, 1000]
n_trials = 20
results_3 = {}

for k in removal_counts:
    diffs = []
    for trial in range(n_trials):
        np.random.seed(42 * trial + k)
        indices = np.random.choice(len(primes_3), size=k, replace=False)
        gapped = np.delete(primes_3, indices)
        spec_gap = mertens_spectroscope(gapped, gammas_3)
        rel_diff = np.sqrt(np.sum((spec_gap - spec_full)**2)) / norm_full * 100
        diffs.append(rel_diff)
    results_3[k] = (np.mean(diffs), np.std(diffs))
    print(f"  k={k}: mean={np.mean(diffs):.3f}%, std={np.std(diffs):.3f}%")

t1 = time.time()
print(f"  Done in {t1-t0:.1f}s")

report("### Results")
report()
report("| Primes Removed | Mean Rel. L2 Diff (%) | Std Dev (%) |")
report("|----------------|----------------------|-------------|")
for k in removal_counts:
    m, s = results_3[k]
    report(f"| {k} | {m:.3f} | {s:.3f} |")

report()

# Detect threshold
threshold = 5.0  # 5% relative change
threshold_k = None
for k in removal_counts:
    if results_3[k][0] >= threshold:
        threshold_k = k
        break

if threshold_k:
    report(f"At a **{threshold}% relative L2 threshold**, degradation becomes significant at **~{threshold_k} removed primes** ({threshold_k/100:.0f}% of total).")
else:
    report(f"Even removing {removal_counts[-1]} primes, the relative L2 change stays below {threshold}%.")

report()

# Also compute single-trial spectrum comparison for k=100
np.random.seed(999)
indices_100 = np.random.choice(len(primes_3), size=100, replace=False)
gapped_100 = np.delete(primes_3, indices_100)
spec_gap_100 = mertens_spectroscope(gapped_100, gammas_3)

fig, axes = plt.subplots(2, 1, figsize=(14, 10))

ax = axes[0]
ax.plot(gammas_3, spec_full, 'b-', alpha=0.7, linewidth=0.5, label='Full (10000 primes)')
ax.plot(gammas_3, spec_gap_100, 'r-', alpha=0.7, linewidth=0.5, label='Gapped (100 removed)')
for z in ZETA_ZEROS[:5]:
    ax.axvline(z, color='green', alpha=0.3, linestyle='--', linewidth=1)
ax.set_xlabel('γ')
ax.set_ylabel('S(γ)')
ax.set_title('Application 3: Full vs Gapped Spectrum (100 primes removed)')
ax.legend()

ax = axes[1]
ks = removal_counts
means = [results_3[k][0] for k in ks]
stds = [results_3[k][1] for k in ks]
ax.errorbar(ks, means, yerr=stds, marker='o', linewidth=2, capsize=5, color='darkblue')
ax.axhline(threshold, color='red', linestyle='--', alpha=0.7, label=f'{threshold}% threshold')
ax.set_xlabel('Number of Primes Removed')
ax.set_ylabel('Relative L2 Difference (%)')
ax.set_title('Application 3: Spectral Degradation vs Missing Data')
ax.set_xscale('log')
ax.legend()
ax.grid(True, alpha=0.3)

plt.tight_layout()
fig.savefig(FIG_DIR / "app3_anomaly_detection.png", dpi=150)
plt.close(fig)
report("![Application 3](figures/app3_anomaly_detection.png)")
report()

report("### Feasibility: HIGH")
report()
report("The spectroscope shows smooth, monotonic degradation as primes are removed.")
