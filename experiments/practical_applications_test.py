# APPLICATION 2: SPECTRAL ESTIMATION ON IRREGULAR GRIDS
# ══════════════════════════════════════════════════════════════════════
report("## Application 2: Spectral Estimation on Irregular Grids")
report()
report("### Method")
report("- Embed signal f(t) = sin(14.13·t) + 0.5·sin(21.02·t) + noise(σ=0.3) at t = log(p) for primes p ≤ 10000.")
report("- Compare three methods: (a) spectroscope, (b) Lomb-Scargle, (c) FFT with interpolation.")
report()

from scipy.signal import lombscargle
from scipy.interpolate import interp1d

primes_2 = sieve_primes(10000)
log_p = np.log(primes_2.astype(float))

np.random.seed(42)
signal = np.sin(14.13 * log_p) + 0.5 * np.sin(21.02 * log_p) + 0.3 * np.random.randn(len(log_p))

gammas_2 = np.arange(0.1, 50.01, 0.05)

print("Computing Application 2: Spectral estimation...")
t0 = time.time()

# (a) Spectroscope: sum f(log(p)) * cos(gamma * log(p))
spec_a = np.zeros(len(gammas_2))
for i, g in enumerate(gammas_2):
    spec_a[i] = np.sum(signal * np.cos(g * log_p))

# (b) Lomb-Scargle (angular frequencies)
angular_freqs = gammas_2  # These are already angular frequencies
spec_b = lombscargle(log_p, signal - signal.mean(), angular_freqs, normalize=True)

# (c) FFT with interpolation
t_uniform = np.linspace(log_p.min(), log_p.max(), 4096)
interp_func = interp1d(log_p, signal, kind='linear', fill_value='extrapolate')
signal_uniform = interp_func(t_uniform)
dt = t_uniform[1] - t_uniform[0]
fft_vals = np.abs(np.fft.rfft(signal_uniform))
fft_freqs = np.fft.rfftfreq(len(t_uniform), d=dt) * 2 * np.pi  # Angular frequency
# Interpolate FFT result onto our gamma grid
mask_fft = fft_freqs <= 50
if len(fft_freqs[mask_fft]) > 1:
    fft_interp = interp1d(fft_freqs[mask_fft], fft_vals[:len(fft_freqs[mask_fft])],
                           kind='linear', fill_value=0, bounds_error=False)
    spec_c = fft_interp(gammas_2)
else:
    spec_c = np.zeros(len(gammas_2))

t1 = time.time()
print(f"  Done in {t1-t0:.1f}s")

# Peak detection
true_freqs = [14.13, 21.02]

def find_peaks_near(spectrum, gammas, target, window=2.0):
    mask = (gammas >= target - window) & (gammas <= target + window)
    if not np.any(mask):
        return target, 0.0
    idx = np.argmax(np.abs(spectrum[mask]))
    return gammas[mask][idx], np.abs(spectrum[mask][idx])

report("### Results")
report()
report(f"- Samples: {len(primes_2)} primes up to 10,000")
report(f"- Log-prime range: [{log_p[0]:.3f}, {log_p[-1]:.3f}]")
report()
report("| Method | Peak 1 (true=14.13) | Error 1 | Peak 2 (true=21.02) | Error 2 |")
report("|--------|---------------------|---------|---------------------|---------|")

for name, spec in [("Spectroscope", spec_a), ("Lomb-Scargle", spec_b), ("FFT+Interp", spec_c)]:
    g1, v1 = find_peaks_near(spec, gammas_2, 14.13)
    g2, v2 = find_peaks_near(spec, gammas_2, 21.02)
    e1 = abs(g1 - 14.13)
    e2 = abs(g2 - 21.02)
    report(f"| {name} | {g1:.2f} | {e1:.2f} | {g2:.2f} | {e2:.2f} |")

report()

# Plot
fig, axes = plt.subplots(3, 1, figsize=(14, 12), sharex=True)

for ax, (name, spec) in zip(axes, [("Spectroscope", spec_a),
                                     ("Lomb-Scargle", spec_b),
                                     ("FFT+Interp", spec_c)]):
    ax.plot(gammas_2, np.abs(spec), 'b-', linewidth=0.7)
    for tf in true_freqs:
        ax.axvline(tf, color='red', alpha=0.6, linestyle='--', linewidth=1, label=f'True: {tf}')
    ax.set_ylabel('|S(γ)|')
    ax.set_title(f'Application 2: {name}')
    ax.legend(loc='upper right')

axes[-1].set_xlabel('γ')
plt.tight_layout()
fig.savefig(FIG_DIR / "app2_spectral_estimation.png", dpi=150)
plt.close(fig)
report("![Application 2](figures/app2_spectral_estimation.png)")
report()

# Assess
g1_s, _ = find_peaks_near(spec_a, gammas_2, 14.13)
g1_l, _ = find_peaks_near(spec_b, gammas_2, 14.13)
g1_f, _ = find_peaks_near(spec_c, gammas_2, 14.13)
e_s = abs(g1_s - 14.13) + abs(find_peaks_near(spec_a, gammas_2, 21.02)[0] - 21.02)
e_l = abs(g1_l - 14.13) + abs(find_peaks_near(spec_b, gammas_2, 21.02)[0] - 21.02)
e_f = abs(g1_f - 14.13) + abs(find_peaks_near(spec_c, gammas_2, 21.02)[0] - 21.02)

report("### Feasibility: MEDIUM")
report()
report(f"- Spectroscope total error: {e_s:.3f}")
report(f"- Lomb-Scargle total error: {e_l:.3f}")
report(f"- FFT total error: {e_f:.3f}")
report()
if e_s < e_f:
    report("The spectroscope outperforms FFT interpolation but is comparable to Lomb-Scargle.")
else:
    report("The spectroscope is comparable to Lomb-Scargle and competitive with FFT.")
report("The prime-based irregular grid is a natural testbed for non-uniform spectral methods.")
report()
report("### Next Steps")
